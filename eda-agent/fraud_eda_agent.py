# Phase 2 — Vertex AI Function Declarations (tool definitions)
# ─────────────────────────────────────────────────────────────────────────────

ADD_MARKDOWN = FunctionDeclaration(
    name="add_markdown_section",
    description="Add a markdown cell — section heading, narrative, or insight bullets.",
    parameters={
        "type": "object",
        "properties": {
            "content": {
                "type": "string",
                "description": "Markdown text to insert into the notebook.",
            }
        },
        "required": ["content"],
    },
)

ADD_CODE = FunctionDeclaration(
    name="add_code_cell",
    description=(
        "Add a Python code cell to the notebook. "
        "Pre-available variables: df, target_col (may be None), "
        "PRIMARY_COLOR, SECONDARY_COLOR, ACCENT_COLOR, pd, np, plt, sns, stats. "
        "End every chart cell with plt.tight_layout() and plt.show()."
    ),
    parameters={
        "type": "object",
        "properties": {
            "code": {
                "type": "string",
                "description": "Runnable Python code to add.",
            },
            "purpose": {
                "type": "string",
                "description": "One-line description of what this cell does.",
            },
        },
        "required": ["code", "purpose"],
    },
)

FINISH = FunctionDeclaration(
    name="finish_notebook",
    description=(
        "Call this once all sections are written to signal the notebook is complete."
    ),
    parameters={
        "type": "object",
        "properties": {
            "summary": {
                "type": "string",
                "description": "2-3 sentence executive summary citing specific numbers.",
            }
        },
        "required": ["summary"],
    },
)

EDA_TOOLS = Tool(function_declarations=[ADD_MARKDOWN, ADD_CODE, FINISH])


# ─────────────────────────────────────────────────────────────────────────────
# Agent
# ─────────────────────────────────────────────────────────────────────────────

class EDAAgent:
    def __init__(
        self,
        project:    Optional[str] = None,
        location:   str = "us-central1",
        model_name: str = "gemini-2.5-flash",
    ):
        """
        Parameters
        ----------
        project    : GCP project ID (falls back to GOOGLE_CLOUD_PROJECT env var)
        location   : Vertex AI region
        model_name : Gemini model to use. Defaults to gemini-2.5-flash.
                     Other options: gemini-2.0-flash-001, gemini-1.5-pro-002
        """
        self.project    = project or os.environ.get("GOOGLE_CLOUD_PROJECT")
        self.location   = location
        self.model_name = model_name
        vertexai.init(project=self.project, location=self.location)

        self._gen_model = GenerativeModel(
            model_name=self.model_name,
            tools=[EDA_TOOLS],
            generation_config=GenerationConfig(temperature=0.2, max_output_tokens=8192),
        )

    def run(
        self,
        df:         pd.DataFrame,
        target_col: Optional[str] = None,
        goal:       Optional[str] = None,
        context:    Optional[str] = None,
        output:     str = "eda_report.ipynb",
        verbose:    bool = True,
    ) -> str:
        """
        Run the two-phase EDA agent and write a .ipynb notebook.

        Parameters
        ----------
        df         : Any tabular DataFrame
        target_col : Optional explicit target column name.
                     If omitted, Gemini infers the best one (or none).
        goal       : Optional plain-text analysis goal.
                     e.g. "analyse serve efficiency and win rates by surface"
        context    : Optional plain-text dataset description.
                     Explain what the dataset is, what the columns mean,
                     any domain-specific knowledge, coded values, etc.
                     e.g. "ATP tennis matches 2000-2023. The 'w_ace' column is
                     winner ace count. Surface codes: H=Hard, C=Clay, G=Grass."
        output     : Output path for the .ipynb file
        verbose    : Print progress

        Returns
        -------
        Absolute path to the saved notebook.
        """
        if verbose:
            print(f"▶ Dataset: {len(df):,} rows × {len(df.columns)} columns")
            print("▶ Computing statistics...")

        summary = _compute_summary(df, target_col)

        # ── Phase 1: planning ────────────────────────────────────────────────
        plan = _run_planning(
            model=self._gen_model,
            model_name=self.model_name,
            summary=summary,
            user_goal=goal,
            user_target=target_col,
            user_context=context,
            verbose=verbose,
        )

        # Resolve final target from plan if not explicitly provided
        resolved_target = target_col or plan.get("target_col")
        title           = plan.get("title", "EDA Report")

        nb = NotebookBuilder(title=title)

        # Data loading cell — self-contained CSV embed
        nb.add_code(f"""\
            import io
            _csv = {repr(df.to_csv(index=False)[:200_000])}
            df = pd.read_csv(io.StringIO(_csv))
            target_col = {repr(resolved_target)}
            print(f"Loaded {{len(df):,}} rows × {{len(df.columns)}} columns")
            if target_col and target_col in df.columns:
                print(f"Target: {{target_col}}")
                print(df[target_col].value_counts().to_string())
        """)

        # ── Phase 2: notebook generation ─────────────────────────────────────
        if verbose:
            print("  → Phase 2: generating notebook cells...")

        gen_prompt  = _build_generation_prompt(plan, summary)
        chat        = self._gen_model.start_chat(response_validation=False)
        pending_msg = gen_prompt   # first turn: plain string
        done        = False
        cell_count  = 0

        while not done:
            if verbose:
                print(f"  → Gemini writing (cells: {cell_count})...")

            response    = chat.send_message(pending_msg)
            fn_parts    = []   # collect Part objects for this turn

            # Check finish reason — 2 = MAX_TOKENS, handle gracefully
            candidate = response.candidates[0]
            finish_reason = getattr(candidate, "finish_reason", None)
            if finish_reason and str(finish_reason) in ("2", "FinishReason.MAX_TOKENS"):
                if verbose:
                    print("  ⚠ Response hit token limit — processing partial response and continuing...")

            for part in candidate.content.parts:
                # Gemini 2.5 may include thought/text parts — skip non-function parts
                fc = getattr(part, "function_call", None)
                if not fc or not getattr(fc, "name", None):
                    continue

                fn   = fc.name
                args = dict(fc.args)

                if fn == "add_markdown_section":
                    nb.add_markdown(args["content"])
                    cell_count += 1
                    if verbose:
                        preview = args["content"][:70].replace("\n", " ")
                        print(f"    + markdown : {preview}…")
                    fn_parts.append(self._fn_resp(fn, "added"))

                elif fn == "add_code_cell":
                    nb.add_code(args["code"])
                    cell_count += 1
                    if verbose:
                        print(f"    + code     : {args.get('purpose', '')}")
                    fn_parts.append(self._fn_resp(fn, "added"))

                elif fn == "finish_notebook":
                    nb.add_markdown(
                        f"---\n## Executive summary\n\n{args['summary']}\n\n"
                        f"*Domain: {plan.get('domain_label', plan.get('domain'))} · "
                        f"Generated by EDAAgent · Vertex AI / Gemini 2.0 Flash*"
                    )
                    done = True
                    if verbose:
                        print("  → finish_notebook called.")
                    fn_parts.append(self._fn_resp(fn, "complete"))

            if fn_parts:
                # Send all function responses as a single Content object
                pending_msg = Content(
                    role="user",
                    parts=fn_parts,
                )
            else:
                if verbose:
                    print("  → No more function calls — saving.")
                done = True

        nb.save(output)
        if verbose:
            print(f"\n✓ {cell_count} cells written  |  domain: {plan.get('domain_label')}")
            print(f"  Open: jupyter notebook {output}")
        return str(Path(output).resolve())

    @staticmethod
    def _fn_resp(name: str, result: str) -> "Part":
        """Return a Vertex AI Part containing a function response."""
        return Part.from_function_response(
            name=name,
            response={"result": result},
        )
