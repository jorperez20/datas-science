"""
eda_agent.py  —  Vertex AI edition  (domain-agnostic)
======================================================
Agentic EDA using Gemini 2.0 Flash on Vertex AI.
Works with ANY tabular dataset: fraud, tennis, sales, medical, etc.

Two-phase design
----------------
Phase 1 — Planning:
    Gemini inspects a sample of the data and returns a structured JSON plan:
    domain, target column, task type (classification / regression / unsupervised),
    analysis goals, and the ordered list of notebook sections to generate.

Phase 2 — Generation:
    Gemini uses that plan as context to write every notebook cell —
    charts, statistics, and narrative are all tailored to the detected domain.

Usage:
    from eda_agent import EDAAgent
    import pandas as pd

    df = pd.read_csv("atp_matches.csv")
    agent = EDAAgent(project="my-gcp-project")
    agent.run(df, goal="analyse serve and win-rate patterns by surface")

    df2 = pd.read_csv("transactions.csv")
    agent.run(df2, target_col="is_fraud")   # explicit target still works

Authentication:
    Local  : gcloud auth application-default login
    GCP    : automatic via attached service account

Requirements:
    pip install -r requirements.txt
"""

