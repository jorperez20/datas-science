class NotebookBuilder:
    def __init__(self, title: str = "EDA Report"):
        self.nb = nbf.new_notebook()
        self.nb.metadata["kernelspec"] = {
            "display_name": "Python 3",
            "language": "python",
            "name": "python3",
        }
        self._add_setup_cell()
        self.add_markdown(
            f"# {title}\n"
            "*Auto-generated by EDAAgent — Vertex AI / Gemini 2.0 Flash*"
        )

    def _add_setup_cell(self):
        self.nb.cells.append(nbf.new_code_cell(textwrap.dedent("""\
            import pandas as pd
            import numpy as np
            import matplotlib.pyplot as plt
            import seaborn as sns
            from scipy import stats
            import warnings
            warnings.filterwarnings('ignore')

            plt.rcParams.update({
                'figure.facecolor': '#0f1923',
                'axes.facecolor':   '#0f1923',
                'axes.edgecolor':   '#2a3f55',
                'axes.labelcolor':  '#8ab4cc',
                'axes.titlecolor':  '#c8dce8',
                'axes.titlesize':   13,
                'axes.labelsize':   11,
                'xtick.color':      '#5a7a8a',
                'ytick.color':      '#5a7a8a',
                'text.color':       '#c8dce8',
                'grid.color':       '#1e2d3a',
                'grid.linewidth':   0.5,
                'legend.facecolor': '#0f1923',
                'legend.edgecolor': '#2a3f55',
                'figure.dpi':       110,
            })
            PRIMARY_COLOR   = '#00aaff'
            SECONDARY_COLOR = '#ff4d6d'
            ACCENT_COLOR    = '#00e5a0'
            print("✓ Setup complete")
        """)))

    def add_markdown(self, text: str):
        self.nb.cells.append(nbf.new_markdown_cell(text))

    def add_code(self, code: str):
        self.nb.cells.append(nbf.new_code_cell(textwrap.dedent(code)))

    def save(self, path: str):
        Path(path).write_text(nbformat.writes(self.nb), encoding="utf-8")
        print(f"✓ Notebook saved → {path}")


# ─────────────────────────────────────────────────────────────────────────────
# Statistics helpers
# ─────────────────────────────────────────────────────────────────────────────

def _compute_summary(df: pd.DataFrame, target: Optional[str]) -> dict:
    """
    Compute dataset statistics independently of domain.
    If target is provided, include per-class breakdowns.
    """
    num_cols = [
        c for c in df.select_dtypes(include="number").columns
        if c != target
    ]
    cat_cols = list(df.select_dtypes(include=["object", "category"]).columns)

    # ── numeric stats ────────────────────────────────────────────────────────
    num_stats = []
    for col in num_cols:
        vals = df[col].dropna()
        if len(vals) == 0:
            continue
        entry = {
            "col":         col,
            "mean":        round(float(vals.mean()), 4),
            "median":      round(float(vals.median()), 4),
            "std":         round(float(vals.std()), 4),
            "min":         round(float(vals.min()), 4),
            "max":         round(float(vals.max()), 4),
            "p25":         round(float(vals.quantile(0.25)), 4),
            "p75":         round(float(vals.quantile(0.75)), 4),
            "skew":        round(float(vals.skew()), 3),
            "missing_pct": round(df[col].isna().mean() * 100, 2),
        }

        # per-class breakdown if target is known and binary/categorical
        if target and target in df.columns:
            for cls_val in df[target].dropna().unique()[:5]:  # cap at 5 classes
                g = df[df[target] == cls_val][col].dropna()
                if len(g):
                    entry[f"mean__{cls_val}"] = round(float(g.mean()), 4)

            # Mann-Whitney for binary targets
            classes = df[target].dropna().unique()
            if len(classes) == 2:
                g1 = df[df[target] == classes[0]][col].dropna()
                g2 = df[df[target] == classes[1]][col].dropna()
                if len(g1) and len(g2):
                    try:
                        _, pval = scipy_stats.mannwhitneyu(
                            g1, g2, alternative="two-sided"
                        )
                        entry["mw_pval"] = round(float(pval), 6)
                        entry["lift"] = (
                            round(float(g1.mean() / g2.mean()), 3)
                            if g2.mean() != 0 else None
                        )
                    except Exception:
                        pass

        num_stats.append(entry)

    # ── categorical stats ────────────────────────────────────────────────────
    cat_stats = []
    for col in cat_cols[:10]:
        freq = df[col].value_counts().head(10).to_dict()
        entry = {"col": col, "n_unique": int(df[col].nunique()), "top_values": freq}
        if target and target in df.columns:
            # value counts per target class for top categories
            top_cats = list(freq.keys())[:6]
            per_class = {}
            for cls_val in df[target].dropna().unique()[:5]:
                sub = df[df[target] == cls_val][col]
                per_class[str(cls_val)] = sub.value_counts().reindex(
                    top_cats, fill_value=0
                ).to_dict()
            entry["per_class"] = per_class
        cat_stats.append(entry)

    # ── target stats (if available) ──────────────────────────────────────────
    target_stats = None
    if target and target in df.columns:
        t = df[target].dropna()
        is_numeric_target = pd.api.types.is_numeric_dtype(t)
        if is_numeric_target and t.nunique() > 10:
            target_stats = {
                "type": "continuous",
                "mean": round(float(t.mean()), 4),
                "std":  round(float(t.std()), 4),
                "min":  round(float(t.min()), 4),
                "max":  round(float(t.max()), 4),
            }
        else:
            vc = t.value_counts()
            target_stats = {
                "type":        "categorical",
                "n_classes":   int(t.nunique()),
                "class_counts": vc.to_dict(),
                "class_pcts":   (vc / len(t) * 100).round(2).to_dict(),
            }

    # ── sample rows (helps Gemini understand the data) ───────────────────────
    sample = df.head(5).to_dict(orient="records")
    # sanitize for JSON
    for row in sample:
        for k, v in row.items():
            if isinstance(v, float) and (np.isnan(v) or np.isinf(v)):
                row[k] = None

    missing = {
        c: round(df[c].isna().mean() * 100, 2)
        for c in df.columns if df[c].isna().any()
    }

    return {
        "shape":        {"rows": len(df), "cols": len(df.columns)},
        "columns":      list(df.columns),
        "dtypes":       {c: str(df[c].dtype) for c in df.columns},
        "target":       target,
        "num_stats":    num_stats,
        "cat_stats":    cat_stats,
        "target_stats": target_stats,
        "missing":      missing,
        "sample_rows":  sample,
    }


# ─────────────────────────────────────────────────────────────────────────────
# Phase 1 — Planning prompt & parser
# ─────────────────────────────────────────────────────────────────────────────

PLANNING_PROMPT = """You are a senior data scientist. You will receive a JSON summary of a dataset
and must return a structured analysis plan so a second AI pass can generate
the right notebook for this specific dataset.

Return ONLY valid JSON — no markdown, no prose, no code fences. Schema:

{{
  "title":        "Short descriptive report title",
  "domain":       "One of: fraud, sports, sales, medical, finance, marketing, hr, logistics, generic",
  "domain_label": "Human-friendly domain name, e.g. Tennis Match Statistics",
  "task_type":    "One of: binary_classification, multiclass_classification, regression, unsupervised",
  "target_col":   "The most meaningful target column name, or null if none",
  "target_label": "Human-friendly name for the target, e.g. Match Winner or null",
  "analysis_goals": ["3-5 specific goals for this dataset and domain"],
  "key_features": ["4-6 column names most likely to be predictive or interesting"],
  "sections": [
    {{
      "id":    "unique_snake_case_id",
      "title": "Section title",
      "type":  "one of: overview, target_distribution, numeric_distributions, categorical_breakdown, correlation, feature_importance, time_series, geographic, domain_specific, insights",
      "description": "What this section should show and why it matters for this domain"
    }}
  ],
  "domain_insights_prompt": "A domain-specific instruction for the AI writing the insights section, e.g. 'Focus on serve efficiency, break-point conversion, and surface differences'"
}}

Rules:
- sections must include at minimum: overview, target_distribution (if target exists),
  numeric_distributions, categorical_breakdown, correlation, and insights.
- Add domain_specific sections when the data warrants it (e.g. win/loss streaks for sports,
  cohort analysis for sales, survival curves for medical).
- key_features should be column names that actually exist in the dataset.
- If no obvious target column exists, set task_type to unsupervised and target_col to null.
- For the target_col: if the user provided one, use it. Otherwise infer the most meaningful one
  from column names and value distributions in the stats.
"""


def _run_planning(
    model: GenerativeModel,
    model_name: str,
    summary: dict,
    user_goal: Optional[str],
    user_target: Optional[str],
    user_context: Optional[str],
    verbose: bool,
) -> dict:
    """Phase 1: ask Gemini to produce a structured analysis plan."""
    context_block = ""
    if user_context:
        context_block = (
            "\n\n## Dataset description (provided by the user)\n"
            + user_context
            + "\nUse this to resolve column name ambiguity, understand what values "
            "represent, and tailor the analysis sections to this specific dataset."
        )
    target_hint = (
        f"\nTarget column (explicitly set by user): '{user_target}'"
        if user_target else ""
    )
    goal_hint = (
        f"\nAnalysis goal (set by user): \"{user_goal}\""
        if user_goal else ""
    )
    # Trim summary for planning — only needs structure, not full per-class stats
    # This prevents truncation on wide datasets with many numeric columns
    planning_summary = {
        "shape":        summary["shape"],
        "columns":      summary["columns"],
        "dtypes":       summary["dtypes"],
        "target":       summary["target"],
        "target_stats": summary["target_stats"],
        "missing":      summary["missing"],
        "sample_rows":  summary["sample_rows"][:3],
        # Slim numeric stats — just col name, mean, std, missing for planning
        "num_stats": [
            {k: v for k, v in s.items()
             if k in ("col", "mean", "std", "min", "max", "missing_pct", "skew", "lift", "mw_pval")}
            for s in summary["num_stats"]
        ],
        # Slim cat stats — just col name and top 5 values
        "cat_stats": [
            {"col": s["col"], "n_unique": s["n_unique"],
             "top_values": dict(list(s["top_values"].items())[:5])}
            for s in summary["cat_stats"]
        ],
    }

    prompt = (
        f"{PLANNING_PROMPT}"
        f"{context_block}\n\n"
        "Dataset statistics:\n"
        f"```json\n{json.dumps(planning_summary, indent=2)}\n```"
        f"{target_hint}{goal_hint}\n\n"
        "Return the analysis plan JSON now."
    )

    if verbose:
        print("  → Phase 1: planning...")

    planning_model = GenerativeModel(
        model_name=model_name,  # same model as generation
        generation_config=GenerationConfig(temperature=0.1, max_output_tokens=4096),
    )
    resp = planning_model.generate_content(prompt)
    raw  = resp.text.strip()

    # Strip markdown code fences if present
    if "```" in raw:
        raw = "\n".join(
            line for line in raw.splitlines()
            if not line.strip().startswith("```")
        ).strip()

    # Robust parse — handle truncated responses gracefully
    try:
        plan = json.loads(raw)
    except json.JSONDecodeError:
        if verbose:
            print("  → Planning response truncated, retrying with slim summary...")
        slim_summary = {
            "shape":       summary["shape"],
            "columns":     summary["columns"],
            "dtypes":      summary["dtypes"],
            "target":      summary["target"],
            "sample_rows": summary["sample_rows"][:3],
            "missing":     summary["missing"],
        }
        retry_prompt = (
            f"{PLANNING_PROMPT}"
            f"{context_block}\n\n"
            "Dataset statistics (condensed):\n"
            f"```json\n{json.dumps(slim_summary, indent=2)}\n```"
            f"{target_hint}{goal_hint}\n\n"
            "Return the analysis plan JSON now. Keep section descriptions brief."
        )
        resp2 = planning_model.generate_content(retry_prompt)
        raw2  = resp2.text.strip()
        if "```" in raw2:
            raw2 = "\n".join(
                line for line in raw2.splitlines()
                if not line.strip().startswith("```")
            ).strip()
        plan = json.loads(raw2)


    if verbose:
        print(f"  → Domain detected: {plan.get('domain_label', plan.get('domain'))}")
        print(f"  → Task type: {plan.get('task_type')}")
        print(f"  → Target: {plan.get('target_col')}")
        print(f"  → Sections planned: {len(plan.get('sections', []))}")

    return plan


def _build_generation_prompt(plan: dict, summary: dict) -> str:
    sections_text = "\n".join(
        f"  {i+1}. [{s['type']}] {s['title']} — {s['description']}"
        for i, s in enumerate(plan.get("sections", []))
    )
    return f"""You are a senior data scientist generating a Jupyter notebook EDA report.

Domain: {plan.get('domain_label', plan.get('domain'))}
Task type: {plan.get('task_type')}
Target column: {plan.get('target_col') or 'None (unsupervised)'}
Target label: {plan.get('target_label') or 'N/A'}

Analysis goals:
{chr(10).join(f'  - {g}' for g in plan.get('analysis_goals', []))}

Key features to focus on: {', '.join(plan.get('key_features', []))}

Sections to generate (in order):
{sections_text}

Domain-specific insight guidance:
{plan.get('domain_insights_prompt', 'Provide relevant statistical insights for this domain.')}

Dataset statistics for reference:
```json
{json.dumps(summary, indent=2)}
```

Rules:
- Generate cells in the exact section order above.
- Every code cell must be self-contained and runnable.
- Use PRIMARY_COLOR / SECONDARY_COLOR / ACCENT_COLOR for all charts.
- Reference specific numbers from the stats in every markdown narrative cell.
- For the target column use: target_col variable (already defined, may be None — check before use).
- For domain_specific sections, write creative, domain-appropriate analysis
  (e.g. for tennis: serve % by surface; for fraud: velocity patterns; for sales: cohort curves).
- Call finish_notebook last with a sharp, specific executive summary.
"""


